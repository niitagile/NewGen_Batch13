
public class Datatypes {

	public static void main(String[] args) {
	
		byte a=56;//smalest int type 1 byte 0 to 255 or -128 to 127
		short b=89;//2 bytes
		int c=100;//4 bytes
		long d=6879879889L;//8 bytes
		
		float e=67.56f; //4 bytes
		double f=67.45; //8 bytes
		
		char g='h'; //2 bytes as java supports unicode
		boolean h=true; // 1 byte
	}

}
