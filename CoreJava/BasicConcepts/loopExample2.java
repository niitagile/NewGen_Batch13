
public class loopExample2 {

	public static void main(String[] args) {
		//break
		for(int num=1;num<=5;num++) {
			
			if(num==3)
				break;
			System.out.println(num);
			
		}
			

		//continue
		for(int num=1;num<=5;num++) {
			
			if(num==3)
				continue;
			System.out.println(num);
		}
		
	}

}
